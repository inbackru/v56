<!-- Описание ЖК -->
<div class="prose max-w-none">
<h3 class="font-bold text-lg mb-3">О комплексе</h3>
<p>Жилой комплекс "Покорители" - это современный квартал комфорт-класса в развивающемся районе Краснодара. Комплекс состоит из 5 монолитно-кирпичных домов высотой от 17 до 25 этажей.</p>
<h3 class="font-bold text-lg mt-6 mb-3">Архитектура</h3>
<p>Дома построены по индивидуальному проекту с панорамным остеклением лоджий и балконов. Фасады выполнены в современном стиле с использованием керамогранита и декоративных элементов.</p>
<h3 class="font-bold text-lg mt-6 mb-3">Территория</h3>
<p>Благоустроенная закрытая территория площадью 2,5 га с детскими площадками, зонами отдыха и прогулочными аллеями. Во дворах полностью отсутствует транспорт - все машины размещаются в подземном паркинге.</p>
<h3 class="font-bold text-lg mt-6 mb-3">Инфраструктура</h3>
<p>На первых этажах расположены кафе, магазины и сервисы. В шаговой доступности - школа, детский сад и поликлиника. В 5 минутах езды - ТРЦ "МЕГА" и парк "Краснодар".</p>

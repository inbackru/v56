                    el.innerHTML = '<span class="typewriter-text">' + staticPrefix + part + '</span>';
                    
                    if (part === fullChangingText) {
                        isDeleting = true;
                        setTimeout(tick, pauseAfterType);
                    } else {
                        setTimeout(tick, typingSpeed);
                    }
